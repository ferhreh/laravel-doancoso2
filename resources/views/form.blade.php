                <ul class="info-ul">
                    <li><span class="info-bold">Parfum:</span> Đây là loại dầu thơm rất đắt tiền vì chúng chứa 20 - 30% tinh chất nên có độ lưu hương lâu hơn. Theo đó, người dùng sẽ sử dụng ít hơn nên thường được bán với dung tích nhỏ.</li>
                    <li><span>Eau de Parfum (EDP):</span> Eau de Parfum cũng khá đậm đặc và lưu hương lâu khi chứa 15 - 20% tinh chất nước hoa. Đây là loại thơm lâu được dùng phổ biến nhất hiện nay vì người dùng có thể sử dụng trong nhà hay ở không gian ngoài trời.</li>
                    <li><span>Eau de Toilette (EDT):</span> Eau de Toilette có nồng độ tinh chất nước hoa từ 10 - 15% với độ bền hương ở mức tương đối. EDT cũng là loại được nhiều người ưa chuộng bởi giá thành hợp lý với mùi hương nhẹ nhàng, thanh thoát.</li>
                    <li><span>Eau de Cologne:</span> Loại dầu thơm này thường chứa khoảng 5 - 7% tinh chất nước hoa. Eau de Cologne thích hợp sử dụng ở nơi có khí hậu nóng và dùng trong không gian hẹp, ít phải hoạt động ngoài trời nên thuộc loại dành cho mùa hè.</li>
                    <li><span>Eau Fraiche:</span> Theo đó, nước hoa sẽ có nồng độ tinh dầu dưới 3%. Eau Fraiche thường có mùi hương nhẹ nhàng, lưu hương chưa đầy 1 tiếng nên là loại nước hoa giá rẻ nhất.</li>
                </ul>
