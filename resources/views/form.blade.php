                <h2>Thành phần có trong nước hoa hàng hiệu</h2>
                <p>Dầu thơm chính hãng thường bao gồm các thành phần như cồn, tinh dầu và một số hoạt chất hóa học khác. Mỗi thành phần sẽ có một vai trò riêng nhưng khi kết hợp lại sẽ tạo nên mùi hương riêng biệt. Cụ thể, các nhãn hàng nước hoa chiết chính hãng sẽ không thể bỏ qua các thành phần chính như: </p>
                <ul class="info-ul" >
                    <li><span>Dầu nền:</span> Dầu dẫn hay dầu nền thường được chiết xuất từ các loại hạt và các loại quả. Dầu nền thường không có màu, không có mùi hoặc có mùi rất nhẹ và lành tính nên được sử dụng thêm khi điều chế sản phẩm nước hoa.</li>
                    <li><span>Tinh dầu:</span> Tinh dầu tồn tại ở dạng lỏng và chứa các hợp chất thơm dễ bay hơi. Các loại tinh dầu thường được chiết xuất từ thân, lá, vỏ, rễ cây hoặc những bộ phận khác của thực vật. Mỗi loại tinh dầu sẽ có hương thơm đặc trưng, khi kết hợp với nhau sẽ tạo ra mùi hương độc đáo. </li>
                    <li><span>Cồn:</span> Cồn được coi là thành phần chủ yếu, chiếm khoảng 50 - 70% bảng thành phần của một chai nước hoa đạt chuẩn. Theo đó, cồn sẽ giúp bảo quản mùi hương, hỗ trợ lan toả hương thơm vô cùng hiệu quả.</li>
                    <li><span>Nước:</span> Thông thường, perfume sẽ chứa từ 50 - 70% là cồn, phần còn lại gồm có dầu nền, nước, tinh dầu và một số hợp chất hóa học khác. Tuy không chiếm tỉ lệ cao nhưng nước cũng là thành phần không thể thiếu khi điều chế nước hoa.</li>
                    <li><span>Dung môi:</span> Đây là chất có công dụng nhũ hóa tinh dầu, nước, hương liệu và các hợp chất hóa học mà không làm đục màu sản phẩm. Dung môi cũng có tác dụng giữ mùi hương nước hoa trên da được lâu lơn.</li>
                </ul>
